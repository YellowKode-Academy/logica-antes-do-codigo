# Capítulo 1 — Instruções São Tudo
# Computadores executam instruções exatas, na ordem certa.

# Exemplo 1: Fazer café — cada instrução importa
print("=== Fazendo Café ===")
print("1. Encher a chaleira com água")
print("2. Ligar a chaleira")
print("3. Esperar a água ferver")
print("4. Colocar o pó de café no coador")
print("5. Despejar a água quente sobre o pó")
print("6. Aguardar o café coar")
print("7. Servir na xícara")
print("Café pronto!")

