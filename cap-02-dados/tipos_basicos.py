# declarando uma variavel de cada tipo
nome = "Kelvin"         # str: texto entre aspas
idade = 32              # int: numero inteiro sem aspas
altura = 1.78           # float: numero decimal com ponto
ativo = True            # bool: True ou False, sem aspas

# exibindo os valores com print
print(nome)
print(idade)
print(altura)
print(ativo)

# usando f-string para formatar a saida em uma frase
print(f"Ola, {nome}! Voce tem {idade} anos e {altura}m de altura.")
